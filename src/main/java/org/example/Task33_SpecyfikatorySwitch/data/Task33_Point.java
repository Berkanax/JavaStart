package org.example.Task33_SpecyfikatorySwitch.data;

public class Task33_Point {
    private int x;
    private int y;

    public Task33_Point() {
    }

    public Task33_Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
