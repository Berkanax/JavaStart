package org.example.Task24_Specyfikatory.data;

public class Task24_Point {
    private int x;
    private int y;

    public Task24_Point() {
    }

    public Task24_Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
